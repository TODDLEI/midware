package net.terakeet.soapware.handlers.report;

import java.util.Calendar;

/**
 * Typesafe Enumeration for period types.
 * @author Administrator
 */
public class PeriodType {
    
    public static final PeriodType MINUTELY = new PeriodType(Calendar.MINUTE,"Minutely");
    public static final PeriodType HOURLY = new PeriodType(Calendar.HOUR_OF_DAY,"Hourly");
    public static final PeriodType DAILY = new PeriodType(Calendar.DAY_OF_YEAR,"Daily");
    public static final PeriodType WEEKLY = new PeriodType(Calendar.WEEK_OF_YEAR,"Weekly");
    public static final PeriodType MONTHLY = new PeriodType(Calendar.MONTH,"Monthly");
    public static final PeriodType QUARTERLY = new PeriodType(-1,"Quarterly");
    public static final PeriodType HALFYEARLY = new PeriodType(Calendar.YEAR,"HalfYearly");
    public static final PeriodType YEARLY = new PeriodType(Calendar.YEAR,"Yearly");
    public static final PeriodType FULL = new PeriodType(-1,"Full");
    
    private int calendarField;
    private String s;
    
    /** Creates a new instance of PeriodType */
    private PeriodType(int calendarField, String name) {
        this.calendarField = calendarField;
        this.s = name;
    }
    
    public int getCalendarField() {
        return calendarField;
    }
    
    public String toString() {
        return s;
    }
    
    public static PeriodType parseString(String s) {
        PeriodType pt = null;
        if (MINUTELY.toString().equalsIgnoreCase(s)) {
            pt = MINUTELY;
        } else if (HOURLY.toString().equalsIgnoreCase(s)) {
            pt = HOURLY;
        } else if (DAILY.toString().equalsIgnoreCase(s)) {
            pt = DAILY;
        } else if (WEEKLY.toString().equalsIgnoreCase(s)) {
            pt = WEEKLY;
        } else if (MONTHLY.toString().equalsIgnoreCase(s)) {
            pt = MONTHLY;
        } else if (QUARTERLY.toString().equalsIgnoreCase(s)) {
            pt = QUARTERLY;
        } else if (HALFYEARLY.toString().equalsIgnoreCase(s)) {
            pt = HALFYEARLY;
        } else if (YEARLY.toString().equalsIgnoreCase(s)) {
            pt = YEARLY;
        } else if (FULL.toString().equalsIgnoreCase(s)) {
            pt = FULL;
        }
        return pt;
    }
}
